fun main(args: Array<String>) {
    var a = 10
    var b = 5
    print("Suma: ")
    println( a + b )

    print("Resta: ")
    println( a - b )

    print("Multiplicacion: ")
    println( a * b )

    print("Divicion: ")
    println( a / b )

    print("El modulo (resto): ")
    println( a % b )
}
